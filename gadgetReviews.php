        <script>
                const blogPostsContainer = document.getElementById('reviewContainer');
                const categoryFilter = document.querySelector('select');
                const searchInput = document.querySelector('input[type="text"]');

                function filterAndSearch() {
                    const selectedCategory = categoryFilter.value.toLowerCase();
                    const searchQuery = searchInput.value.toLowerCase();

                    const blogPosts = blogPostsContainer.querySelectorAll('div[data-category]');
                    blogPosts.forEach(post => {
                        const category = post.getAttribute('data-category').toLowerCase();
                        const name = post.getAttribute('data-name').toLowerCase();

                        if (
                            (selectedCategory === 'all categories' || category.includes(selectedCategory)) &&
                            (name.includes(searchQuery) || searchQuery === '')
                        ) {
                            post.style.display = 'block';
                        } else {
                            post.style.display = 'none';
                        }
                    });
                }

                categoryFilter.addEventListener('change', filterAndSearch);
                searchInput.addEventListener('input', filterAndSearch);
            </script>




